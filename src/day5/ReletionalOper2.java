package day5;

public class ReletionalOper2 {
    public static void main(String[] args) {

        System.out.println(1000==2000);
        System.out.println("Java"=="Java");
        System.out.println("Java"=="java");
        System.out.println(true==false);
        System.out.println("---------------------------------");

        System.out.println(1000!=2000);
        System.out.println("Java"!="Java");
        System.out.println("Java"!="java");
        System.out.println(true!=false);



    }
}
