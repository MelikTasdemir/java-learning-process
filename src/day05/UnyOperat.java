package day05;

public class UnyOperat {
    public static void main(String[] args) {


        int x = 10;

        System.out.println(++x);
        System.out.println(x);

int y = 100;
        System.out.println(--y);
        System.out.println(y);
        System.out.println("--------------------------------");

        int z = 50;

        System.out.println(z++);
        System.out.println(z);

        int t = 75;

        System.out.println(t--);
        System.out.println(t);
        System.out.println("--------------------------------");


        int u = 30;
        int m = u++;
        System.out.println(m);
        System.out.println(u);

    }
}
